RANGE = re.compile(r"(\d{2})월\s*(\d{2})일\([^)]\)\s*~\s*(\d{2})월\s*(\d{2})일\([^)]\)")
SINGLE = re.compile(r"(\d{2})월\s*(\d{2})일\([^)]\)")


def holidays():
    out = set()
    for block in RAW.strip().split("\n"):
        block = block.strip()
        if not block:
            continue
        y = int(block[:4])
        body = block[5:]
        # ranges first, then blank them out so SINGLE does not re-read the ends
        for m in RANGE.finditer(body):
            m1, d1, m2, d2 = (int(g) for g in m.groups())
            a = pd.Timestamp(y, m1, d1)
            b = pd.Timestamp(y, m2, d2)
            for d in pd.date_range(a, b, freq="D"):
                out.add(d)
        body = RANGE.sub(" ", body)
        for m in SINGLE.finditer(body):
            mm, dd = int(m.group(1)), int(m.group(2))
            out.add(pd.Timestamp(y, mm, dd))
    return pd.DatetimeIndex(sorted(out))


